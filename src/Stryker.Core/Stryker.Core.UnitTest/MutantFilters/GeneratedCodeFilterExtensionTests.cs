using System.IO.Abstractions.TestingHelpers;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.VisualStudio.TestTools.UnitTesting;
using Shouldly;
using Stryker.Core.MutantFilters;

namespace Stryker.Core.UnitTest.MutantFilters;

[TestClass]
public class GeneratedCodeFilterExtensionTests : TestBase
{
    [TestMethod]
    [DataRow("Component.razor.g.cs")]
    [DataRow("App.razor.g.cs")]
    [DataRow("MyView.cshtml.g.cs")]
    [DataRow("Generated.g.cs")]
    public void IsSourceGeneratorOutput_ReturnsTrue_ForDotGDotCsFiles(string fileName)
    {
        var result = GeneratedCodeFilterExtension.IsSourceGeneratorOutput(fileName);

        result.ShouldBeTrue();
    }

    [TestMethod]
    [DataRow("Component.razor.G.CS")]
    [DataRow("Generated.G.CS")]
    public void IsSourceGeneratorOutput_IsCaseInsensitive(string fileName)
    {
        var result = GeneratedCodeFilterExtension.IsSourceGeneratorOutput(fileName);

        result.ShouldBeTrue();
    }

    [TestMethod]
    [DataRow("Form1.designer.cs")]
    [DataRow("Settings.Designer.cs")]
    [DataRow("Program.cs")]
    [DataRow("MyClass.cs")]
    public void IsSourceGeneratorOutput_ReturnsFalse_ForNonGeneratorOutputFiles(string fileName)
    {
        var result = GeneratedCodeFilterExtension.IsSourceGeneratorOutput(fileName);

        result.ShouldBeFalse();
    }

    [TestMethod]
    public void IsSourceGeneratorOutput_OnSyntaxTree_ReturnsTrue_ForDotGDotCsPath()
    {
        var filePath = @"C:\Project\obj\debug\net8.0\Component.razor.g.cs";
        var syntaxTree = CSharpSyntaxTree.ParseText("", path: filePath);

        var result = syntaxTree.IsSourceGeneratorOutput();

        result.ShouldBeTrue();
    }

    [TestMethod]
    public void IsSourceGeneratorOutput_OnSyntaxTree_ReturnsFalse_ForDesignerFile()
    {
        var filePath = @"C:\Project\Form1.designer.cs";
        var syntaxTree = CSharpSyntaxTree.ParseText("", path: filePath);

        var result = syntaxTree.IsSourceGeneratorOutput();

        result.ShouldBeFalse();
    }

    [TestMethod]
    public void IsSourceGeneratorOutput_OnSyntaxTree_ReturnsFalse_ForRegularFile()
    {
        var filePath = @"C:\Project\Program.cs";
        var syntaxTree = CSharpSyntaxTree.ParseText("", path: filePath);

        var result = syntaxTree.IsSourceGeneratorOutput();

        result.ShouldBeFalse();
    }

    [TestMethod]
    public void IsSourceGeneratorOutput_UsesFileSystemAbstraction()
    {
        var fileSystem = new MockFileSystem();
        var filePath = @"C:\Project\obj\Component.razor.g.cs";

        var result = GeneratedCodeFilterExtension.IsSourceGeneratorOutput(filePath, fileSystem);

        result.ShouldBeTrue();
    }

    [TestMethod]
    [DataRow("Form1.designer.cs")]
    [DataRow("Settings.Designer.cs")]
    public void IsGenerated_ReturnsTrue_ForGeneratedFileNames(string fileName)
    {
        // Only *.designer.cs files are matched by the file-name check;
        // *.g.cs source-generator output is detected via IsSourceGeneratorOutput(),
        // not via IsGeneratedFileName, to avoid false positives in test projects.
        var filePath = $@"C:\Project\{fileName}";
        var syntaxTree = CSharpSyntaxTree.ParseText("// regular content", path: filePath);

        var result = syntaxTree.IsGenerated();

        result.ShouldBeTrue();
    }

    [TestMethod]
    public void IsGenerated_ReturnsFalse_ForRegularFile()
    {
        var filePath = @"C:\Project\Program.cs";
        var syntaxTree = CSharpSyntaxTree.ParseText("// regular content", path: filePath);

        var result = syntaxTree.IsGenerated();

        result.ShouldBeFalse();
    }

    [TestMethod]
    public void IsGenerated_ReturnsFalse_ForDotGDotCsWithoutAutoGeneratedComment()
    {
        // *.g.cs files that lack an <auto-generated> header must NOT be treated as generated
        // by IsGenerated(), because test-runner source generators (e.g. Microsoft.Testing.Platform.MSBuild
        // used by NUnit3TestAdapter 6.x) produce *.g.cs files without that header, and
        // TestProject.cs relies on IsGenerated() to decide which files belong in TestFiles.
        // Source-generator output in the TARGET project is caught by IsSourceGeneratorOutput()
        // in CsharpProjectComponentsBuilder before IsGenerated() is checked.
        var filePath = @"C:\Project\AssemblyInfo.g.cs";
        var syntaxTree = CSharpSyntaxTree.ParseText("// regular content", path: filePath);

        var result = syntaxTree.IsGenerated();

        result.ShouldBeFalse();
    }

    [TestMethod]
    public void IsGenerated_ReturnsTrue_WhenAutoGeneratedCommentPresent()
    {
        const string source = """
            // <auto-generated/>
            public class Foo { }
            """;
        var syntaxTree = CSharpSyntaxTree.ParseText(source, path: @"C:\Project\Regular.cs");

        var result = syntaxTree.IsGenerated();

        result.ShouldBeTrue();
    }

    [TestMethod]
    public void IsGenerated_ReturnsTrue_WhenAutogeneratedCommentPresent_LegacyFormat()
    {
        const string source = """
            // <autogenerated/>
            public class Foo { }
            """;
        var syntaxTree = CSharpSyntaxTree.ParseText(source, path: @"C:\Project\Regular.cs");

        var result = syntaxTree.IsGenerated();

        result.ShouldBeTrue();
    }
}
